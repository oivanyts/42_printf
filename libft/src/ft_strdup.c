#include "../includes/libft.h"

char	*ft_strdup(const char *src)
{
	size_t		i;
	size_t		len;
	char		*str;

	len = ft_strlen(src);
	if (!(str = (char*)malloc(sizeof(*str) * (len + 1))))
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
